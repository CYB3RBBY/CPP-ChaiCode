#include <bits/stdc++.h>
using namespace std;

// Taking inputs and operators 
int main(){
    float a , b; // typecasting because can't use division operation on integer values
    cout << "Enter first number:" << endl;
    cin >> a;
    cout << "Enter second number:" << endl;
    cin >> b;
    
    cout << "The sum is " << a + b << endl;
    cout << "a + b is: " << a + b << endl;
    cout << "a - b is: " << a - b << endl;
    cout << "a * b is: " << a * b << endl;
    cout << "a / b is: " << a / b << endl;

    return 0;
}